// Helper to get item by ID
export function getItemById(id: number): ItemTemplate | undefined {
  return itemTemplates.find((i) => i.id === id);
}

// Helper to get item by keyword
export function getItemByKeyword(keyword: string): ItemTemplate | undefined {
  const lowerKeyword = keyword.toLowerCase();
  return itemTemplates.find((i) =>
    i.name.toLowerCase().includes(lowerKeyword) ||
    i.keywords.some((k) => k.toLowerCase().includes(lowerKeyword))
  );
}
