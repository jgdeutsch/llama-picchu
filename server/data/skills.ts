// Helper to get skill by ID
export function getSkillById(id: number): SkillDefinition | undefined {
  return skillDefinitions.find((s) => s.id === id);
}

// Helper to get skill by slug
export function getSkillBySlug(slug: string): SkillDefinition | undefined {
  return skillDefinitions.find((s) => s.slug === slug);
}

// Get skills available for a class
export function getClassSkills(classId: number): SkillDefinition[] {
  return skillDefinitions.filter((s) => s.classRequired === classId || !s.classRequired);
}
